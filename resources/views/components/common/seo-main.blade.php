            <li>
                <p class=" font-montmedium text-[13px] md:text-sm lg:text-base xl:text-lg mt-3 xl:mt-8">On-the-Page search ranking factors are those that are almost entirely within the publisher’s own control. What type of content do you publish? Are you providing important HTML clues that help search engines (and users) determine relevancy? How does your site architecture help or hinder search engines?</p>
            </li>
            <li>
                <p class=" font-montmedium text-[13px] md:text-sm lg:text-base xl:text-lg mt-3 xl:mt-8">Off-the-Page ranking factors are those that publishers do not directly control. Search engines use these because they learned early on that relying on publisher-controlled signals alone didn’t always yield the best results. For instance, some publishers may try to make themselves seem more relevant than they are in reality.
                </p>
            </li>

